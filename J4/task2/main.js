// Створіть веб-програму, в якій користувач вводить ширину та символ. Вивести в консоль лінію заданої ширини
// заданим символом
let w;
do {
    w = Number(prompt("Введите ширину"));
    if (Number.isNaN (w)){
        alert("Попробуйте еще раз!");
    }
} while (Number.isNaN(w));
let s = prompt("Введите символ");
let str = '';
for (let i = 0; i < w; i++){
    str += s;
}
console.log(str);

