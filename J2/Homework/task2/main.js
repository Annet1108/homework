// 2. Средняя прибыль
// Создайте веб-приложение, которое спрашивает у пользователя прибыль за каждый месяц квартала, а потом выводит среднюю прибыль за этот период.
let a = Number(prompt('прибыль за первый месяц'))
let b = Number(prompt('прибыль за второй месяц'))
let c = Number(prompt('прибыль за третий месяц'))
let average = (a + b + c) / 3
alert("Средняя прибыль за этот период: " + average)

